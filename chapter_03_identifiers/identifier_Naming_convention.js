// Camel Case - first word lowercase, each subsequent word capitalized
let firstName = "John";
let totalAmount = 500;
let getUserName = "Alice";

// Pascal Case - every word starts with a capital letter
let FirstName = "John";
let TotalAmount = 500;
let GetUserName = "Alice";

// Snake Case - words separated by underscores
let first_name = "John";
let total_amount = 500;
let get_user_name = "Alice";

// Screaming Snake Case - all uppercase with underscores
let FIRST_NAME = "John";
let TOTAL_AMOUNT = 500;
let MAX_LIMIT = 100;

// Kebab Case - words separated by hyphens (used in HTML/CSS, not valid in JS variables)
// first-name  => invalid in JS
// total-amount => invalid in JS

// Hungarian Notation - prefix indicates type
let strName = "John";
let intAge = 25;
let boolIsActive = true;
